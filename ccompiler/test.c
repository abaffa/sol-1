int ackermann(int m, int n) {
    if (m == 0) {
        return n + 1;
    } else if (n == 0) {
        return ackermann(m - 1, 1);
    } else {
        return ackermann(m - 1, ackermann(m, n - 1));
    }
}

int main() {
    int m; m = 4;
    int n; n = 3;
    int result;

    result = ackermann(m, n);

    printn("The result is: ", result);
    print("\n");

    return 0;
}



